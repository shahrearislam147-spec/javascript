// Working with two numbers
let a = 10;
let b = 5;

// Expression: sum of two numbers
let sum = a + b; // '+' is the operator, 'a + b' is the expression

// Expression: product of two numbers
let product = a * b; // '*' is the operator, 'a * b' is the expression

console.log("Sum:", sum);       // Output: Sum: 15
console.log("Product:", product); // Output: Product: 50
